// Comments are a way to add information to your code. They explain the code and make it easier to understand.
// Comments are not executed by the browser or wherever you run your code.

// Single-line comments start with two forward slashes (//) and continue until the end of the line.
// They are usually used to explain a single line of code or to disable a line of code temporarily.
// Example:
thisIsCode(); // This is a single-line comment explaining that what you see on the left is theoretically a function call.

// Multi-line comments start with a forward slash and an asterisk (/*) and end with an asterisk and a forward slash (*/).
// They are usually used to explain a block of code or to disable a block of code temporarily.
// They can also be used to add documentation to your code, which will become more important later on.
// Example:
/*
 * This is a multi-line comment.
 * It can contain multiple lines.
 */
thisIsCode();

// Disabling code: To disable a line of code, or multiple lines, you can just add a comment at the beginning of the line or around a block of code.
// Example:

// Normal Code
doSomething();
thisIsTotally();
valid();
code();

// Single Line
// doSomething();
thisIsTotally();
valid();
code();

