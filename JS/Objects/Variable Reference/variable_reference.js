    /*
        In Javascript, Some Data Types Are Passed By Reference,
        Meaning That When You Assign A Variable To That Data Type,
        The Variable Holds A Reference To The Data In Memory Rather Than The Actual Data Itself.
        
        This Means That Changes Made To The Referenced Data Will Also Affect The Variable Holding The Reference.

        You May Have Seen A Comment Hinting On This Within The Spread Operator Section
        What I Was Trying To Say: If I Would've Assigned The Same Reference, Both Variables Would've Pointed
        To The Same "Data" In Memory
        
        Meaning:
        If One Variable Changes A Value, That Change Applies To The Other Variable Pointing To The Same Memory Space.

        The Following Types Are Passed By Reference:
        - Functions
        - Objects
        - Classes

        In Contrast These Are Types Passed By Value Rather Than Reference:
        - Numbers
        - Strings
        - Booleans
        - Null
        - Undefined
    */